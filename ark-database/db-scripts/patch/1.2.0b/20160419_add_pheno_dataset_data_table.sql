use `pheno`;
DROP TABLE IF EXISTS `pheno_dataset_data`;
CREATE TABLE `pheno_dataset_data` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `PHENO_DATASET_FIELD_DISPLAY_ID` int(11) NOT NULL,
  `PHENO_DATASET_COLLECTION_ID` int(11) NOT NULL,
  `DATE_DATA_VALUE` date DEFAULT NULL,
  `NUMBER_DATA_VALUE` double DEFAULT NULL,
  `TEXT_DATA_VALUE` text,
  `ERROR_DATA_VALUE` text,
  PRIMARY KEY (`ID`),
  KEY `FK_PHENO_DATASET_DATA_PDF_DISPLAY_idx` (`PHENO_DATASET_FIELD_DISPLAY_ID`),
  KEY `FK_PHENO_DATASET_DATA_PDF_COLLECTION_idx` (`PHENO_DATASET_COLLECTION_ID`),
  CONSTRAINT `FK_PHENO_DATASET_DATA_PDF_COLLECTION` FOREIGN KEY (`PHENO_DATASET_COLLECTION_ID`) REFERENCES `pheno_dataset_collection` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `FK_PHENO_DATASET_DATA_PDF_DISPLAY` FOREIGN KEY (`PHENO_DATASET_FIELD_DISPLAY_ID`) REFERENCES `pheno_dataset_field_display` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

