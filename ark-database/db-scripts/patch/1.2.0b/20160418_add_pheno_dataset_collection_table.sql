use `pheno`;
DROP TABLE IF EXISTS `pheno_dataset_collection`;
CREATE TABLE `pheno_dataset_collection` (
  `ID` int(11) NOT NULL AUTO_INCREMENT,
  `LINK_SUBJECT_STUDY_ID` int(11) NOT NULL,
  `PHENO_DATASET_FIELD_GROUP_ID` int(11) NOT NULL,
  `QUESTIONNAIRE_STATUS_ID` int(11) NOT NULL,
  `REVIEWED_BY_ID` int(11) NOT NULL,
  `RECORD_DATE` date DEFAULT NULL,
  `REVIEWED_DATE` date DEFAULT NULL,
  `DESCRIPTION` varchar(500) DEFAULT NULL,
  PRIMARY KEY (`ID`),
  KEY `FK_PHENO_COLLECTION_LINK_SUBJECT_STUDY_ID_idx` (`LINK_SUBJECT_STUDY_ID`),
  KEY `FK_PHENO_COLLECTION_PHENO_DATASET_FIELD_GROUP_ID_idx` (`PHENO_DATASET_FIELD_GROUP_ID`),
  KEY `FK_PHENO_QUESTIONNAIRE_STATUS_ID_idx` (`QUESTIONNAIRE_STATUS_ID`),
  KEY `FK_PHENO_COLL_REVIEWED_ID_idx` (`REVIEWED_BY_ID`),
  CONSTRAINT `FK_PHENO_COLL_REVIEWED_ID` FOREIGN KEY (`REVIEWED_BY_ID`) REFERENCES `study`.`ark_user` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `FK_PHENO_COLL_LINK_PHE_DATSET_GP_ID` FOREIGN KEY (`PHENO_DATASET_FIELD_GROUP_ID`) REFERENCES `pheno_dataset_field_group` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `FK_PHENO_COLL_LINK_SUB_STUDY_ID` FOREIGN KEY (`LINK_SUBJECT_STUDY_ID`) REFERENCES `study`.`link_subject_study` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION,
  CONSTRAINT `FK_PHENO_COLL_QUES_STAT_ID` FOREIGN KEY (`QUESTIONNAIRE_STATUS_ID`) REFERENCES `questionnaire_status` (`ID`) ON DELETE NO ACTION ON UPDATE NO ACTION
) ENGINE=InnoDB DEFAULT CHARSET=latin1;


